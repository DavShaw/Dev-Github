#Hoy aprenderemos: Parámetros End y Sep



#El parámetro end permite evitar el salto de línea cuando se usan dos print()
print("")
print("")

print("Cuando NO se usa el end")
print("Esto es lo que pasa")

print("")
print("")
print("")

print("Cuando SÍ se usa el end", end=" ")
print("Esto es lo que pasa")


#El parametro sep permite eliminar los espacios dentro de un print cuando hay cadena de caracteres, es decir: print("Hola", "Como", "Estás")
print("")
print("")


print('Sin sep=""')
print("1","2","3","4","5")

print("")
print("")

print('Con sep=""')
print("1","2","3","4","5", sep="")

