    private void transferButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_transferButtonActionPerformed
        
        String destinationUserDni = JOptionPane
        .showInputDialog(
            this,
            "Enter destination user dni: ",
            "Transfer",
            JOptionPane.QUESTION_MESSAGE);

        String amount = JOptionPane
        .showInputDialog(
            this,
            "Enter the amount: ",
            "Transfer",
            JOptionPane.QUESTION_MESSAGE);

        if(Checker.isDigit(destinationUserDni) && Checker.isDigit(amount)) {

            Integer destinationUserDniInt = Integer.parseInt(destinationUserDni);
            Double amountDouble = Double.parseDouble(amount);

            ResultPack<Boolean> r1 = AccountController.accountExist(destinationUserDniInt);
            
            if(r1.getResult()) {

                ResultPack<Boolean> r2 = AccountTransferController.transfer(this.userDni, destinationUserDniInt, amountDouble);
                JOptionPane.showMessageDialog(this, r2.getMessageFormatted(), "Transfer", JOptionPane.INFORMATION_MESSAGE);
            }
            else {
                JOptionPane.showMessageDialog(this, r1.getMessageFormatted(), "Transfer", JOptionPane.ERROR_MESSAGE);
            }
            

        }
        else {
            JOptionPane.showMessageDialog(this, "Invalid number.", "Transfer", JOptionPane.ERROR_MESSAGE);
        }
        

    }//GEN-LAST:event_transferButtonActionPerformed

    private void leaveTeamButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_leaveTeamButtonActionPerformed
        
        String teamId = JOptionPane.showInputDialog(this, "Enter team id:", "Leave team", JOptionPane.QUESTION_MESSAGE);

        if(Checker.isDigit(teamId)) {

            ResultPack<Boolean> r1 = UserController.leaveTeam(this.userDni, Integer.parseInt(teamId));
            JOptionPane.showMessageDialog(this, r1.getMessageFormatted(), "Transfer", JOptionPane.INFORMATION_MESSAGE);
        }
        else {
            JOptionPane.showMessageDialog(this, "Invalid number.", "Transfer", JOptionPane.ERROR_MESSAGE);
        }


    }//GEN-LAST:event_leaveTeamButtonActionPerformed

