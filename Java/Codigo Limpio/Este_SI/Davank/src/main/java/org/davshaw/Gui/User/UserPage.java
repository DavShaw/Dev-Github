    private void changePasswordButtonActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_changePasswordButtonActionPerformed
        
        String oldPassword = JOptionPane.showInputDialog(this, "Enter your old password: ", "Change password", JOptionPane.QUESTION_MESSAGE);
        String newPassword = JOptionPane.showInputDialog(this, "Enter your new password: ", "Change password", JOptionPane.QUESTION_MESSAGE);

        oldPassword = oldPassword.trim();
        newPassword = newPassword.trim();

        ResultPack<Boolean> r1 = UserController.changePassword(this.userDni, oldPassword, newPassword);

        if(r1.getOkay()) {
            JOptionPane.showMessageDialog(this, r1.getMessageFormatted(), "Change password", JOptionPane.INFORMATION_MESSAGE);
        }
        else {
            JOptionPane.showMessageDialog(this, r1.getMessageFormatted(), "Change password", JOptionPane.ERROR_MESSAGE);
        }



    }//GEN-LAST:event_changePasswordButtonActionPerformed

