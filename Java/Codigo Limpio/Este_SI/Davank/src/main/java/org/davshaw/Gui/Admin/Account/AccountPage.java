  private void deleteAccountButtonActionPerformed(
    java.awt.event.ActionEvent evt
  ) {//GEN-FIRST:event_deleteAccountButtonActionPerformed
    String dniStr = JOptionPane.showInputDialog(
      this,
      "Enter user Dni: ",
      "Delete Account",
      JOptionPane.PLAIN_MESSAGE
    );
    if (dniStr != null) {
      if (Checker.isDigit(dniStr)) {
        int dni = Integer.parseInt(dniStr);
        ResultPack<Boolean> r1 = UserController.userExist(dni);
        if (r1.getResult()) {
          ResultPack<Boolean> r2 = AccountController.deleteAccount(dni);
          String message = String.format(
            "Account deleted successfully: %s\nServer return: %s\nMessage: %s",
            r2.getOkay(),
            r2.getResult(),
            r2.getMessage()
          );

          JOptionPane.showMessageDialog(
            this,
            message,
            "Delete Account",
            JOptionPane.INFORMATION_MESSAGE
          );
        } else {
          JOptionPane.showMessageDialog(
            this,
            r1.getMessage(),
            "Delete account",
            JOptionPane.ERROR_MESSAGE
          );
        }
      } else {
        JOptionPane.showMessageDialog(
          this,
          "Invalid Dni.",
          "Delete Account",
          JOptionPane.ERROR_MESSAGE
        );
      }
    }
  }//GEN-LAST:event_deleteAccountButtonActionPerformed

